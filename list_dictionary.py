# Python Script demonstrating list operations

# 1. Create a list names 'fruits', with elements 'apple',
Fruits = ['apple', 'banana', 'cherry', ]

# 2. Print the entire list
print("Original List of Fruits:", Fruits)

# 3. print first and last elements
print("First fruit in the list:", Fruits[0])
print("last fruit in the list:", Fruits[-1])

# 4. add 'grape' to the list and print the update list
Fruits.append('grape')
print("Updated list after adding grape:", Fruits)


